            <div class="mpl-content-wrap">
            <div class="wp-block-image"><figure class="alignleft is-resized"><img src="https://rkzsurabaya.com/wp-content/uploads/2019/09/2731901-1-1024x683.jpg" alt="" class="wp-image-8678" srcset="https://rkzsurabaya.com/wp-content/uploads/2019/09/2731901-1-1024x683.jpg 1024w, https://rkzsurabaya.com/wp-content/uploads/2019/09/2731901-1-300x200.jpg 300w, https://rkzsurabaya.com/wp-content/uploads/2019/09/2731901-1-768x512.jpg 768w" sizes="(max-width: 183px) 100vw, 183px" width="183" height="122"></figure></div>
                <p>Penggunaan obat selama kehamilan sering menjadi dilema. Di satu sisi, ibu hamil mungkin membutuhkan obat untuk mengobati penyakit yang muncul selama kehamilan atau penyakit yang sudah diderita sebelum hamil. Namun di sisi lain, muncul kekhawatiran bahwa obat yang digunakan bisa membahayakan janin</p>
            <h4 class="text-center">BAGAIMANA OBAT YANG DIGUNAKAN IBU HAMIL DAPAT SAMPAI KE JANIN?</h4>
            <br><br>
            <div class="wp-block-image"><figure class="aligncenter is-resized"><img src="https://rkzsurabaya.com/wp-content/uploads/2019/08/2019-08-28-GAMBAR-KEAMANAN-OBAT-PADA-IBU-HAMIL-1024x713.jpg" alt="" class="wp-image-8659" width="311" height="215"></figure></div>
                <h4 class="text-center">APA SAJA EFEK OBAT PADA JANIN</h4>
                <br><br>
            <ol><li>Obat berefek secara langsung pada janin, sehingga menyebabkan kelainan, cacat, atau kematian. Contoh : Obat antikejang seperti Fenitoin dan Fenobarbital menyebabkan cacat bawaan, kerusakan saraf, dan hambatan pertumbuhan janin.</li><li>Obat mempengaruhi jaringan tubuh ibu dan secara tidak langsung berpengaruh pada janin. Contoh : Misoprostol melunakkan mulut rahim, sehingga dapat memicu keguguran.</li><li>Obat menghambat aliran darah melalui ari – ari, sehingga mengganggu pasokan oksigen dan nutrisi ke janin. Contoh obat hipertensi pada dosis yang tinggi mampu menurunan aliran darah ke ari – ari sehingga menyebabkan kekurangan oksigen.</li></ol>
        <p style="text-align:center"><strong>OBAT BEBAS, SUPLEMEN MULTIVITAMIN, DAN OBAT TRADISIONAL (JAMU) TIDAK SEMUANYA AMAN UNTUK IBU HAMIL. SELALU BERKONSULTASI DENGAN DOKTER ATAU APOTEKER UNTUK MEMASTIKAN KEAMANAN OBAT DAN BERAPA DOSIS YANG DIBUTUHKAN KARENA OBAT-OBAT TERTENTU DAPAT BERPENGARUH BURUK TERHADAP JANIN.</strong></p>
        <h4 class="text-center">GEJALA YANG SERING DIJUMPAI SELAMA KEHAMILAN DAN PENGOBATAN YANG AMAN</h4>
            <ol><li><strong>Batuk</strong><ul><li>Minum banyak air hangat.</li><li>Gunakan permen hisap yang mengandung mint, madu atau menthol untuk melegakan tenggorokan.</li><li>Obat yang aman : Dekstrometorfan (untuk batuk kering), atau Gliseril Guaiakolat/Guaifenesin, Bromhexin (untuk batuk berdahak).</li></ul></li><br><br><li><strong>Pilek atau Hidung Tersumbat</strong><ul><li>Hirup uap panas atau gunakan balsem yang mengandung menthol (inhaler). </li></ul></li><br><br><li><strong>Alergi</strong><ul><li>Kenali dan hindari semua hal yang dapat menyebabkan alergi.</li><li>Jika alergi/gatal-gatal di kulit, lebih baik pilih obat luar (krim, salep, atau bedak).</li><li>Oleskan tipis dan gunakan jangka pendek.</li><li>Obat yang aman:<ul><li>Kulit/local : calamine (gejala ringan), Kortikosteoid (gejala berat).</li><li>Obat minum :&nbsp; Klorfeniramin maleat (CTM), Difenhidramin.</li></ul></li></ul></li><br><li> <strong>Nyeri, Pegal-pegal, Sakit Kepala </strong><ul><li>Rendam/kompres air hangat di bagian yang nyeri.</li><li>Obat yang aman : Parasetamol</li></ul></li><br><br><li><strong>Demam </strong><ul><li>Kompres, banyak minum, makanan berkaldu.</li><li>Periksa suhu tubuh secara teratur dengan termometer (jika &gt; 40<sup>o </sup>&nbsp;atau demam tidak turun dalam 2×24 jam, segera berobat ke dokter/RS terdekat).</li><li>Obat yang aman : Parasetamol.</li></ul></li><br><li><strong>Diare</strong><ul><li> Jika baru 1 hari dan gejala tidak berat, biarkan keluar. </li><li>Ganti cairan yang hilang dengan oralit dan makan makanan yang lunak berkuah.</li><li>Jika gejala tidak membaik atau frekuensi diare sering (&gt;5x/hari), segera berobat ke dokter / RS. </li><li>Obat yang aman : Kaolin-pektin, Attapulgit.</li></ul></li><br><br><li><strong>Sembelit</strong><ul><li>Perbanyak makanan berserat (agar-agar, sayur, Buah).</li><li>Obat yang aman :<ul><li>Efek lambat : Psyllium disertai banyak minum;</li><li>Efek cepat : Laktulosa</li></ul></li></ul></li><br><br><li><strong>Keluhan lambung/maag</strong><ul><li>Makan teratur, hindari makanan pedas dan asam.</li><li>Makan dengan porsi kecil tapi sering.</li><li>Obat yang aman : Antasida, Ranitidin.</li></ul></li><br><li><strong>Mual-muntah</strong><ul><li>Obat yang aman : Doxylamine, Pyrathiazine (kedua obat tersebut biasanya dikombinasi dengan vitamin B6 atau Pyridoxine), Difenhidramin, Dimenhidrinat, dan Metoklopramid.</li></ul></li></ol>
            <p style="text-align:center"><strong>HATI-HATI DALAM MEMILIH OBAT BATUK PILEK YANG BERUPA KOMBINASI BEBERAPA MACAM OBAT. BACA BROSUR/LABEL OBAT, PASTIKAN MASING-MASING KOMPOSISINYA AMAN DAN TIDAK MENGANDUNG ALKOHOL ATAU ETHANOL. JIKA TIDAK YAKIN, KONSULTASI DENGAN APOTEKER.</strong></p>
